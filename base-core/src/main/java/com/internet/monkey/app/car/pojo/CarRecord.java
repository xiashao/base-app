public class CarRecord {

    private String id;
    private String carLight;
    private String carMachine;
    private String carShift;
    private String carGas;
    private String recordTime;
    private String carId;
    private String description;
    private String milegag;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCarLight() {
        return carLight;
    }

    public void setCarLight(String carLight) {
        this.carLight = carLight;
    }

    public String getCarMachine() {
        return carMachine;
    }

    public void setCarMachine(String carMachine) {
        this.carMachine = carMachine;
    }

    public String getCarShift() {
        return carShift;
    }

    public void setCarShift(String carShift) {
        this.carShift = carShift;
    }

    public String getCarGas() {
        return carGas;
    }

    public void setCarGas(String carGas) {
        this.carGas = carGas;
    }

    public String getRecordTime() {
        return recordTime;
    }

    public void setRecordTime(String recordTime) {
        this.recordTime = recordTime;
    }

    public String getCarId() {
        return carId;
    }

    public void setCarId(String carId) {
        this.carId = carId;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getMilegag() {
        return milegag;
    }

    public void setMilegag(String milegag) {
        this.milegag = milegag;
    }
}
